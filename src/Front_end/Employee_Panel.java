    public void populateUserComboBox() throws SQLException {
        DbConnect con = new DbConnect();
        con.connect();
        String query = "SELECT Username FROM users WHERE role = 'Customer'";
        PreparedStatement pstmt = (PreparedStatement) con.prepareStatement(query);
        ResultSet rs = pstmt.executeQuery();

        DefaultComboBoxModel<String> comboBoxModel = new DefaultComboBoxModel<>();
        while (rs.next()) {
            String username = rs.getString("username");
            comboBoxModel.addElement(username);
        }

        comboBox.setModel(comboBoxModel);
    }

    public void showCustomerTable(String username) throws SQLException {
        DbConnect con = new DbConnect();
        con.connect();
        int userId = con.getUserIdFromUsername(username);
        DefaultTableModel model = (DefaultTableModel) tableCustomer.getModel();
        String query = "SELECT c.cart_id, c.status AS Status, p.name AS Name, c.quantity AS Quantity, cat.name AS Category, p.price AS 'Unit price' "
                + "FROM cart c "
                + "JOIN products p ON c.product_id = p.product_id "
                + "JOIN categories cat ON p.category_id = cat.category_id "
                + "WHERE c.user_id = ?";
        PreparedStatement pstmt = (PreparedStatement) con.prepareStatement(query);
        pstmt.setInt(1, userId);
        ResultSet rs = pstmt.executeQuery();
        model.setRowCount(0);
        while (rs.next()) {
            String cartId = rs.getString("cart_id");
            String name = rs.getString("Name");
            String category = rs.getString("Category");
            double unitPrice = rs.getDouble("Unit price");
            int Quantity = rs.getInt("quantity");
            String status = rs.getString("Status");
            double totalPrice = unitPrice * Quantity;
            totalPrice = Math.round(totalPrice * 1000.0) / 1000.0;
            Object[] row = {cartId, name, category, unitPrice, Quantity, totalPrice, status};
            model.addRow(row);
        }
    }

