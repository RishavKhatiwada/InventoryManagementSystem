            String productTable = "";
            String productHeader = String.format("| %-" + (productNameWidth + 2) + "s| %-" + (quantityWidth + 2) + "s| %-" + (priceWidth + 3) + "s|\n", "Product Name", "Quantity", "Price");  // remove %.2f format specifier
